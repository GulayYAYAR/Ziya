package Day2_variables;

public class c4_variables {
    public static void main(String[] args) {

//PRIMITIVE DATA TYPES

                //boolean:  it is true or false.  it is 1 bit
boolean bool1=true;
        System.out.println(bool1);
        bool1= false;
        System.out.println(bool1);
                // byte: it is 8 bit values between -128 to 127
byte b1=120;
        System.out.println(b1);
                // char: it is 16 bit. it comes from character. it contains characters
char c1='g';
        System.out.println(c1);
                //short: 16 bits
short s1=20000;
        System.out.println(s1);
                //int: it is short of integers
int i1=420009;
        System.out.println(i1);
                //longs: 64 bits memory, 12L, -2L
long l1=244678999L;
        System.out.println(l1);

        //Float and double for decimal numbers
                // float: 32 bits ,,,,, 1.2f , -32.234f
float f1=2.3412577989987907333f;
        System.out.println(f1);  //output: 2.3412578 sadece 7 tanesi cikar
                //double 64 bits
double d1= 121.34223522323425944354354353453434;
        System.out.println(d1); // output: 121.34223522323425 sadece 14 tanesi cikar

        // NON-PRIMITIVE DAYTA TYPES
        //such as String, loop, class,....



    }
}
